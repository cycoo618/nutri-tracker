/**
 * 在内置食物数据库中搜索食物
 * 支持按名称、英文名、别名、品牌、标签搜索
 */
export function searchFood(query: string): FoodItem[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];

  return FOOD_DATABASE.filter(food =>
    food.name.includes(q) ||
    food.nameEn?.toLowerCase().includes(q) ||
    food.aliases?.some(a => a.includes(q)) ||
    food.brand?.includes(q) ||
    food.tags?.some(t => t.includes(q)) ||
    food.category === q
  ).sort((a, b) => {
    // 精确匹配排前面
    const aExact = a.name.toLowerCase().startsWith(q) ? 0 : 1;
    const bExact = b.name.toLowerCase().startsWith(q) ? 0 : 1;
    return aExact - bExact;
  });
}

/** 按分类获取食物 */
export function getFoodsByCategory(category: string): FoodItem[] {
  return FOOD_DATABASE.filter(f => f.category === category);
}

